def resolve_symbol(text: str) -> str:
    t = text.strip().lower()
    if t in SYMBOL_MAP:
        return SYMBOL_MAP[t]
    # Check if it's a 4-digit Tadawul code
    if t.isdigit() and len(t) == 4:
        return t + ".SR"
    return text.strip().upper()


def friendly_name(symbol: str) -> str:
    return MARKET_NAMES.get(symbol, symbol)


def get_stock_data(symbol: str, period: str = "6mo", interval: str = "1d", fetch_info: bool = True):
    try:
        sym = resolve_symbol(symbol)
        ticker = yf.Ticker(sym)
        hist = ticker.history(period=period, interval=interval)
        if hist.empty:
            return None, None
        info = {}
        if fetch_info:
            try:
                info = ticker.info or {}
            except Exception:
                info = {}
        return hist, info
    except Exception as e:
        logging.error(f"get_stock_data error for {symbol}: {e}")
        return None, None


def get_current_price(symbol: str):
    try:
        sym = resolve_symbol(symbol)
        ticker = yf.Ticker(sym)
        data = ticker.history(period="5d")
        if data.empty or "Close" not in data.columns:
            return None, None, None
        latest = data.iloc[-1]
        price = round(float(latest["Close"]), 2)
        if len(data) > 1:
            prev = data.iloc[-2]
            change = round(float(latest["Close"] - prev["Close"]), 2)
            change_pct = round((change / float(prev["Close"])) * 100, 2)
        else:
            change = 0.0
            change_pct = 0.0
        return price, change, change_pct
    except Exception as e:
        logging.error(f"get_current_price error for {symbol}: {e}")
        return None, None, None


def _batch_prices(symbols):
    cache = {}
    chunks = [symbols[i:i+100] for i in range(0, len(symbols), 100)]
    for chunk in chunks:
        try:
            data = yf.download([s for s in chunk if s], period="5d", progress=False, threads=False)
            if data.empty:
                continue
            closes = data["Close"] if isinstance(data.columns, pd.MultiIndex) else data
            for sym in chunk:
                try:
                    if isinstance(closes, pd.DataFrame) and sym in closes.columns:
                        vals = closes[sym].dropna()
                        if len(vals) >= 2:
                            price = round(float(vals.iloc[-1]), 2)
                            prev = round(float(vals.iloc[-2]), 2)
                            chg = round(price - prev, 2)
                            pct = round((chg / prev) * 100, 2)
                            cache[sym] = (price, chg, pct)
                        elif len(vals) == 1:
                            price = round(float(vals.iloc[-1]), 2)
                            cache[sym] = (price, 0.0, 0.0)
                except Exception as e:
                    logging.error(f"_batch_prices item error {sym}: {e}")
        except Exception as e:
            logging.error(f"_batch_prices chunk error: {e}")
    return cache


def get_market_group(group: str):
    results = []
    syms = MARKET_GROUPS.get(group, [])
    prices = _batch_prices(syms)
    for sym in syms:
        if sym in prices:
            price, chg, pct = prices[sym]
            results.append((friendly_name(sym), sym, price, chg, pct))
    return results


def get_screener():
    now = time.time()
    if hasattr(get_screener, '_cache') and hasattr(get_screener, '_cache_ts') and now - get_screener._cache_ts < 300:
        return get_screener._cache
    results = []
    try:
        prices = _batch_prices(SCREENER_SYMBOLS)
    except Exception:
        prices = {}
    for sym in SCREENER_SYMBOLS:
        if sym in prices:
            price, chg, pct = prices[sym]
            results.append((friendly_name(sym), sym, price, chg, pct))
    results.sort(key=lambda x: abs(x[4]), reverse=True)
    get_screener._cache = results
    get_screener._cache_ts = now
    return results


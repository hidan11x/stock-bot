def _preload_screener():
    try:
        get_screener()
    except Exception:
        pass
_t.Thread(target=_preload_screener, daemon=True).start()


def market_status():
    utc_now = datetime.utcnow()
    # Saudi (UTC+3)
    saudi_h = (utc_now.hour + 3) % 24
    saudi_m = utc_now.minute
    saudi_wd = (utc_now.weekday() + 1) % 7  # Mon=0..Sun=6 -> shift so Sun=0
    # After shift: Sun=0, Mon=1, Tue=2, Wed=3, Thu=4, Fri=5, Sat=6
    saudi_day = saudi_wd <= 4  # Sun-Thu
    saudi_tm = saudi_h * 60 + saudi_m

    # US Eastern (UTC-4 summer / UTC-5 winter) — simplified: use UTC-4
    us_h = (utc_now.hour - 4) % 24
    us_m = utc_now.minute
    us_wd = utc_now.weekday()
    us_tm = us_h * 60 + us_m

    msgs = {}

    # Saudi market: Sun-Thu 10:00-15:00 Riyadh
    if saudi_day and 600 <= saudi_tm <= 900:
        msgs["🇸🇦 السعودية"] = ("🟢 مفتوح", f"يغلق {15 - saudi_h - (1 if saudi_m > 0 else 0):02d}:{(60 - saudi_m) % 60:02d}")
    elif saudi_day and saudi_tm < 600:
        rem = 600 - saudi_tm
        msgs["🇸🇦 السعودية"] = ("🔴 مغلق", f"يفتح بعد {rem // 60}س {rem % 60}د")
    elif saudi_day:
        msgs["🇸🇦 السعودية"] = ("🔴 مغلق", "يفتح غداً 10:00")
    else:
        msgs["🇸🇦 السعودية"] = ("🔴 مغلق", "يفتح الأحد 10:00")

    # US market: Mon-Fri 9:30-16:00 ET
    if 0 <= us_wd <= 4 and 570 <= us_tm <= 960:
        msgs["🇺🇸 أمريكا"] = ("🟢 مفتوح", "")
    else:
        msgs["🇺🇸 أمريكا"] = ("🔴 مغلق", "")

    # Crypto: 24/7
    msgs["₿ عملات"] = ("🟢 مفتوح (24/7)", "")

    # Forex: Sun-Fri 24h
    if 0 <= us_wd <= 4 or us_wd == 6:
        msgs["💱 فوركس"] = ("🟢 مفتوح", "")
    else:
        msgs["💱 فوركس"] = ("🔴 مغلق", "يفتح الأحد")

    return msgs


def get_news(symbol: str, max_items: int = 5):
    try:
        sym = resolve_symbol(symbol)
        ticker = yf.Ticker(sym)
        news = ticker.news
        if not news:
            return []
        items = []
        for article in news[:max_items]:
            title = article.get("title", "")
            link = article.get("link", "")
            publisher = article.get("publisher", "")
            items.append((title, publisher, link))
        return items
    except Exception as e:
        logging.error(f"get_news error for {symbol}: {e}")
        return []
